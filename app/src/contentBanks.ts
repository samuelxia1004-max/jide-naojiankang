function addRotatingChoices(choices: string[], pool: string[], index: number, targetLength = 8) {
  const next = [...choices];
  let offset = 0;
  while (next.length < targetLength && offset < pool.length * 2) {
    const item = pool[(index * 3 + offset) % pool.length];
    if (!next.includes(item)) next.push(item);
    offset += 1;
  }
  return next;
}

